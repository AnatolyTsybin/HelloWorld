#include <stdio.h>

int main()
{
	printf("Hello, World!\n");
	printf("Addition of 32 and 128 is %d\n", 32+128);
	printf("Subtraction of the same numbers is %d\n", 32-128);
	printf("Multiplication result is %d\n", 32*128);
	printf("Division result is %f\n", 32.0/128.0);
	printf("Integer division of 127 by 32 result is %d\n", 127/32);
	printf("Remainder of integer division of 127 by 32 is %d\n", 127%32);
	return 0;
}
